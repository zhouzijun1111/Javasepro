package com.oppo.variable;

public class VariableDemo {
    public static void main(String[] args) {
        int age = 23;
        System.out.println(age);

        double score = 99.5;
        System.out.println(score);

        System.out.println("-----------------------------");

        int money = 20;
        System.out.println(money);
        money = money + 100;
        System.out.println(money);
        money = money - 50;
        System.out.println(money);
    }

}
